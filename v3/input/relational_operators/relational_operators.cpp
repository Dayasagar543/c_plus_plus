#include <iostream>
using namespace std;
int main()
{
    int a = 34, b = 45;
    cout << (a > b) << " if a is greater than b" << endl;
    cout << (a < b) << " if a is less than b" << endl;
    cout << (a >= b) << " if a is greater than equal b" << endl;
    cout << (a <= b) << " if a is less than equal b" << endl;
    cout << (a != b) << " if a is not equal  b" << endl;
    return 0;
}